// Variables(var, let, const)

// var
// init 
var name = 'John Doe';
// redeclare
name = 'John Smith';

// declare
var greeting; // defaults to undefined
// assign
greeting = 'Hello';

// let
// init
let name = 'John Doe';
// reassign
name = 'John Smith';

// const
// init
const name = 'John';
// cannot redeclare
name = 'Jane'; // INVALID
// should always assign a value
const greeting; // INVALID
